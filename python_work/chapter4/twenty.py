numbers=[value+1 for value in range(0,1000000)]
print(numbers)		#Um milhão
print(min(numbers))	#Minimo de um milhao
print(max(numbers))	#Maximo de um milhao
print(sum(numbers))	#Soma de um milhao de numeros (1 a 1.000.000)

numbers=[value+1 for value in range(0,20)]
print(numbers)		#Contando até vinte

numbers=[value for value in range(1,21,2)]
print(numbers)		#Numeros impares de um a vinte

numbers=[value*3 for value in range(1,11)]
print(numbers)		#Lista de multiplos de 3 de 1 a 30

numbers=[value**3 for value in range(1,11)]
print(numbers)		#Lista dos dez primeiros cubos de um a dez
